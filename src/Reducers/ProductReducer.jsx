const ProductReducer = (state, action) => {
  switch (action.type) {
    case "SET_PRODUCT_LOADING":
      return {
        ...state,
        isProductLoading: true,
      };

    case "SET_API_DATA":
      return {
        ...state,
        isProductLoading: false,
        products: action.payload,
      };
    case "API_ERROR":
      return {
        ...state,
        isProductLoading: false,
        isProductError: true,
      };

    // company
    case "SET_COMPANIES_LOADING":
      return {
        ...state,
        isCompanyLoading: true,
      };

    case "SET_COMPANIES_API_DATA":
      return {
        ...state,
        isCompanyLoading: false,
        companies: action.payload,
      };
    case "SET_COMPANIES_API_ERROR":
      return {
        ...state,
        isCompanyLoading: false,
        isCompanyError: true,
      };
    // generic
    case "SET_GENERIC_LOADING":
      return {
        ...state,
        isGenericLoading: true,
      };

    case "SET_GENERIC_API_DATA":
      return {
        ...state,
        isGenericLoading: false,
        generics: action.payload,
      };
    case "SET_GENERIC_API_ERROR":
      return {
        ...state,
        isGenericLoading: false,
        isGenericError: true,
      };
    // category
    case "SET_CATEGORIES_LOADING":
      return {
        ...state,
        isCategoryLoading: true,
      };

    case "SET_CATEGORIES_API_DATA":
      return {
        ...state,
        isCategoryLoading: false,
        categories: action.payload,
      };
    case "SET_CATEGORIES_API_ERROR":
      return {
        ...state,
        isCategoryLoading: false,
        isCategoryError: true,
      };

    default:
      return state;
  }
};

export default ProductReducer;
